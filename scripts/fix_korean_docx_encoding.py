from pathlib import Path
from shutil import copy2

from docx import Document
from docx.enum.text import WD_ALIGN_PARAGRAPH


ROOT = Path(__file__).resolve().parents[1] / "WhatIWrote"


def backup(path: Path, suffix: str) -> None:
    if path.exists():
        copy2(path, path.with_name(path.stem + suffix + path.suffix))


def clear_body(doc: Document) -> None:
    body = doc._body._element
    for child in list(body):
        if child.tag.endswith("sectPr"):
            continue
        body.remove(child)


def add_paragraph(doc: Document, kind: str, text: str = "") -> None:
    p = doc.add_paragraph()
    if kind == "blank":
        return
    run = p.add_run(text)
    if kind == "title":
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        run.bold = True
    elif kind == "author":
        p.alignment = WD_ALIGN_PARAGRAPH.RIGHT
        run.bold = True
    elif kind in {"heading", "ref_heading"}:
        run.bold = True


def rewrite_doc(path: Path, entries: list[tuple[str, str]]) -> None:
    doc = Document(path)
    clear_body(doc)
    for kind, text in entries:
        add_paragraph(doc, kind, text)
    doc.save(path)


